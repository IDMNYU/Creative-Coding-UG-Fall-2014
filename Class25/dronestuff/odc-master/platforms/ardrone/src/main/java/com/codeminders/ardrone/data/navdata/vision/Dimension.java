
package com.codeminders.ardrone.data.navdata.vision;

public class Dimension
{
    private int width;
    private int height;

    public Dimension(int width, int height)
    {
        super();
        this.width = width;
        this.height = height;
    }

    public int getWidth()
    {
        return width;
    }

    public int getHeight()
    {
        return height;
    }
}
